package com.byteshaft.doctor.gettersetter;

public class Agenda {
    String createdAt;
    String date;
    int doctorId;
    String startTIme;
    String endTime;
    int agendaId;
    String reaseon;
    String agendaState;

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(int doctorId) {
        this.doctorId = doctorId;
    }

    public String getStartTIme() {
        return startTIme;
    }

    public void setStartTIme(String startTIme) {
        this.startTIme = startTIme;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public int getAgendaId() {
        return agendaId;
    }

    public void setAgendaId(int agendaId) {
        this.agendaId = agendaId;
    }

    public String getReaseon() {
        return reaseon;
    }

    public void setReaseon(String reaseon) {
        this.reaseon = reaseon;
    }

    public String getAgendaState() {
        return agendaState;
    }

    public void setAgendaState(String agendaState) {
        this.agendaState = agendaState;
    }

}
